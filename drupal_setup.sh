#!/bin/bash
set -e

PROJECT_NAME="${1:?Usage: $0 <project-name> [language]}"
LANGUAGE="${2:-en}"

# Configure ddev
ddev config --project-type=drupal --php-version=8.3 --docroot=web --project-name="$PROJECT_NAME"

# Start ddev
ddev start

# Download Drupal 11 into a temp directory to avoid conflicts with existing files
ddev exec mkdir -p /tmp/drupal-temp
ddev exec composer create-project "drupal/recommended-project:^11" /tmp/drupal-temp --no-install

# Move all Drupal files (including dotfiles) into the project root
ddev exec bash -c 'shopt -s dotglob && cp -r /tmp/drupal-temp/* /var/www/html/ && rm -rf /tmp/drupal-temp'

# Ignore known security advisory for symfony/process (required by drupal/core-recommended)
ddev composer config audit.ignore "PKSA-rkkf-636k-qjb3"

# Install composer dependencies
ddev composer install

# Install Drush
ddev composer require drush/drush

# Install Drupal site
ddev drush site:install standard \
  --site-name="$PROJECT_NAME" \
  --locale="$LANGUAGE" \
  --account-name=admin \
  --account-pass=admin \
  --yes

# Show status
ddev drush status

# Generate .gitignore if it doesn't exist or enhance the existing one
if [ ! -f .gitignore ]; then
  echo "Creating .gitignore..."
  cat > .gitignore << 'EOF'
# Ignore directories generated by Composer
/vendor/
/web/core/
/web/modules/contrib/
/web/themes/contrib/
/web/profiles/contrib/
/web/libraries/

# Ignore Drupal's file directory
/web/sites/*/files/

# Ignore SimpleTest multi-site environment.
/web/sites/simpletest/

# Ignore files generated by PhpStorm
/.idea/

# Ignore .env files as they are personal
/.env

# Ignore Drupal settings that are environment-specific
/web/sites/*/settings.local.php
/web/sites/*/services.local.yml

# Ignore paths that contain user-generated content.
/web/sites/*/private/

# Ignore DDEV-generated files (but keep .ddev/config.yaml in version control)
/.ddev/.gitignore
/.ddev/.dbimageBuild/
/.ddev/.dbimageExtra/
/.ddev/.ddev-docker-*
/.ddev/.homeadditions/
/.ddev/.importdb/
/.ddev/.webimageBuild/
/.ddev/.webimageExtra/
/.ddev/import-db/
/.ddev/import.yaml
/.ddev/db_snapshots/
/.ddev/*.log

# Ignore OS-specific files
.DS_Store
Thumbs.db

# Ignore node_modules if using frontend tools
node_modules/

# Ignore build artifacts
/build/
/dist/
EOF
else
  echo ".gitignore already exists, skipping creation..."
fi

# Initialize Git repository
echo ""
echo "Initializing Git repository..."
if [ ! -d .git ]; then
  git init
  git add .
  git commit -m "Initial commit

Co-Authored-By: Claude Sonnet 4.5 <noreply@anthropic.com>"
  echo "Git repository initialized with initial commit"
else
  echo "Git repository already exists, skipping initialization..."
fi

echo ""
echo "=== Setup complete ==="
echo "URL: $(ddev describe -j | php -r 'echo json_decode(file_get_contents("php://stdin"))->raw->httpurl;' 2>/dev/null || echo "Run 'ddev describe' to check the URL")"
echo "Username: admin"
echo "Password: admin"
